using System;
using System.Text;
using System.Security.Cryptography;
using System.IO;

namespace Common.Infrastructure
{
    public static class Encryption
    {
        public static string Encrypt(string plainText, string key)
        {
            byte[] bytesBuff = Encoding.Unicode.GetBytes(plainText);
            using (Aes aes = Aes.Create())
            {
                Rfc2898DeriveBytes crypto = new Rfc2898DeriveBytes(key, new byte[] { 0x49, 0x76, 0x61, 0x6e, 0x20, 0x4d, 0x65, 0x64, 0x76, 0x65, 0x64, 0x65, 0x76 });
                aes.Key = crypto.GetBytes(32);
                aes.IV = crypto.GetBytes(16);
                using (MemoryStream mStream = new MemoryStream())
                {
                    using (CryptoStream cStream = new CryptoStream(mStream, aes.CreateEncryptor(), CryptoStreamMode.Write))
                    {
                        cStream.Write(bytesBuff, 0, bytesBuff.Length);
                        cStream.Close();
                    }
                    plainText = Convert.ToBase64String(mStream.ToArray());
                }
            }
            return plainText;
        }
        public static string Decrypt(string encryptedText, string key)
        {
            encryptedText = encryptedText.Replace(" ", "+");
            byte[] bytesBuff = Convert.FromBase64String(encryptedText);
            using (Aes aes = Aes.Create())
            {
                Rfc2898DeriveBytes crypto = new Rfc2898DeriveBytes(key, new byte[] { 0x49, 0x76, 0x61, 0x6e, 0x20, 0x4d, 0x65, 0x64, 0x76, 0x65, 0x64, 0x65, 0x76 });
                aes.Key = crypto.GetBytes(32);
                aes.IV = crypto.GetBytes(16);
                using (MemoryStream mStream = new MemoryStream())
                {
                    using (CryptoStream cStream = new CryptoStream(mStream, aes.CreateDecryptor(), CryptoStreamMode.Write))
                    {
                        cStream.Write(bytesBuff, 0, bytesBuff.Length);
                        cStream.Close();
                    }
                    encryptedText = Encoding.Unicode.GetString(mStream.ToArray());
                }
            }
            return encryptedText;
        }
    }
}
